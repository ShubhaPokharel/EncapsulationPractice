package com.pnc.bank;

public class Emp {
	private int sid;
	private String name;
	private int avg;
	private String adress;
	 
	public void setSid(int sid) {
		this.sid = sid;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setAvg(int avg) {
		this.avg = avg;
	}
	public void setAdress(String adress) {
		this.adress = adress;
	}
	
	
	public int getSid() {
		return this.sid;
	}
	public String getName() {
		return this.name;
	}
	public int getAvg() {
		return this.avg;
	}
	public String getAdress() {
		return this.adress;
	}
	
	
	
}
